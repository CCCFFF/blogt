   function equalHeight(group) {
   tallest = 0;
   group.each(function() {
      thisHeight = $(this).height();
      if(thisHeight > tallest) {
         tallest = thisHeight;
      }
   });
   group.height(tallest);
}
$(window).load(function() {
   equalHeight($(".column"));
});
$(window).load(function() {
   equalHeight($(".column1"));
});
